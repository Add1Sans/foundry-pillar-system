1) Copy the entire 'foundry-pillar-system' folder into:
   FoundryVTT/Data/systems/

2) Start Foundry → Select your world → Manage Modules/Systems
   Enable "Pillars of Resolve (Percentile)".

3) In Compendiums, you’ll see:
   - Pillar Talents
   - Weapon Talents
   - Starter Spells
   - Starter Weapons
   - Sample Actors

4) Drag items from compendia onto actor sheets.
   Use chat macros inside items (the 'cast' field) to roll.

If editing compendium files manually:
- Close Foundry first.
- Paste exact JSON arrays into the .db files.
- Remove any comments if present.
- Reopen Foundry; compendiums will load the new data.
